package vo;

public class ProductDTO {
	private int item_num;
	private String item_name;
	private int item_price;
	private String origin;
	private int calorie;
	private String item_info;
	private int item_category;
	private String item_allergie;
	private String item_img;
	private int item_show;
	
	public int getItem_num() {
		return item_num;
	}
	public void setItem_num(int item_num) {
		this.item_num = item_num;
	}
	public String getItem_name() {
		return item_name;
	}
	public void setItem_name(String item_name) {
		this.item_name = item_name;
	}
	public int getItem_price() {
		return item_price;
	}
	public void setItem_price(int item_price) {
		this.item_price = item_price;
	}
	public String getOrigin() {
		return origin;
	}
	public void setOrigin(String origin) {
		this.origin = origin;
	}
	public int getCalorie() {
		return calorie;
	}
	public void setCalorie(int calorie) {
		this.calorie = calorie;
	}
	public String getItem_info() {
		return item_info;
	}
	public void setItem_info(String item_info) {
		this.item_info = item_info;
	}
	public int getItem_category() {
		return item_category;
	}
	public void setItem_category(int item_category) {
		this.item_category = item_category;
	}
	public String getItem_allergie() {
		return item_allergie;
	}
	public void setItem_allergie(String item_allergie) {
		this.item_allergie = item_allergie;
	}
	public String getItem_img() {
		return item_img;
	}
	public void setItem_img(String item_img) {
		this.item_img = item_img;
	}
	public int getItem_show() {
		return item_show;
	}
	public void setItem_show(int item_show) {
		this.item_show = item_show;
	}
	
	
}
