package vo;

public class TableDTO {

	private int table_no;
	
	private int table_x;
	
	private int table_y;
	
	private int table_w;
	
	private int table_h;

	public int getTable_no() {
		return table_no;
	}

	public void setTable_no(int table_no) {
		this.table_no = table_no;
	}

	public int getTable_x() {
		return table_x;
	}

	public void setTable_x(int table_x) {
		this.table_x = table_x;
	}

	public int getTable_y() {
		return table_y;
	}

	public void setTable_y(int table_y) {
		this.table_y = table_y;
	}

	public int getTable_w() {
		return table_w;
	}

	public void setTable_w(int table_w) {
		this.table_w = table_w;
	}

	public int getTable_h() {
		return table_h;
	}

	public void setTable_h(int table_h) {
		this.table_h = table_h;
	}
	
}
